const MORSE_TABLE = {
	'.-': 'a',
	'-...': 'b',
	'-.-.': 'c',
	'-..': 'd',
	'.': 'e',
	'..-.': 'f',
	'--.': 'g',
	'....': 'h',
	'..': 'i',
	'.---': 'j',
	'-.-': 'k',
	'.-..': 'l',
	'--': 'm',
	'-.': 'n',
	'---': 'o',
	'.--.': 'p',
	'--.-': 'q',
	'.-.': 'r',
	'...': 's',
	'-': 't',
	'..-': 'u',
	'...-': 'v',
	'.--': 'w',
	'-..-': 'x',
	'-.--': 'y',
	'--..': 'z',
	'.----': '1',
	'..---': '2',
	'...--': '3',
	'....-': '4',
	'.....': '5',
	'-....': '6',
	'--...': '7',
	'---..': '8',
	'----.': '9',
	'-----': '0',
	' ': ' '
};

const morse = {
	10: '.',
	11: '-',
	00: '',
	'**********': ' '
};

const decode = (expr) =>
	expr
		.match(new RegExp(`.{1,10}`, 'g'))
		.map((el) =>
			el
				.replace(/00/g, '')
				.replace(/10/g, '.')
				.replace(/11/g, '-')
				.replace(/^\*.*$/g, ' ')
		)
		.map((el) => (el = MORSE_TABLE[el]))
		.join('');

module.exports = {
	decode
};
